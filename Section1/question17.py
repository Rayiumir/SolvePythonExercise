import math

# 17: محاسبه مساحت ذوزنقه ای
a = 5  # مثال طول پایه بالایی بر حسب متر
b = 10  # مثال طول پایه کمتر بر حسب متر
h = 4  # ارتفاع مثال بر حسب متر

# فرمول مساحت ذوزنقه: مساحت = (a + b) * h / 2
area = ((a + b) * h) / 2
print("Trapezoidal Area:", area, "square meters")\

# Output > Trapezoidal Area: 30.0 square meters