# دریافت عدد از کاربر و تبدیل آن به float
number = float(input("لطفاً یک عدد وارد کنید: "))

# بررسی اینکه عدد دارای رقم اعشاری است یا خیر
if number == int(number):
    print("عدد وارد شده رقم اعشاری ندارد.")
else:
    print("عدد وارد شده دارای رقم اعشاری است.")

# --------------------------
# مثال خروجی:
# اگر ورودی 10 باشد:
# عدد وارد شده رقم اعشاری ندارد.
#
# اگر ورودی 5.7 باشد:
# عدد وارد شده دارای رقم اعشاری است.
