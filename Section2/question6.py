# دریافت دو عدد از کاربر و تبدیل آن‌ها به نوع float
num1 = float(input("لطفاً عدد اول را وارد کنید: "))
num2 = float(input("لطفاً عدد دوم را وارد کنید: "))

# بررسی اینکه آیا عدد اول بزرگتر یا مساوی عدد دوم است
if num1 >= num2:
    print("عدد اول بزرگتر یا مساوی عدد دوم است.")
else:
    print("عدد اول کوچکتر از عدد دوم است.")

# --------------------------
# مثال خروجی:
# اگر ورودی‌ها 10 و 5 باشند:
# عدد اول بزرگتر یا مساوی عدد دوم است.
#
# اگر ورودی‌ها 3 و 8 باشند:
# عدد اول کوچکتر از عدد دوم است.
