# دریافت دو عدد از کاربر و تبدیل آن‌ها به عدد صحیح
num1 = int(input("لطفاً عدد اول را وارد کنید: "))
num2 = int(input("لطفاً عدد دوم را وارد کنید: "))

# بررسی زوج بودن هر دو عدد
if num1 % 2 == 0 and num2 % 2 == 0:
    print("هر دو عدد زوج هستند.")
else:
    print("هر دو عدد زوج نیستند.")

# --------------------------
# مثال خروجی:
# اگر ورودی‌ها 4 و 8 باشند:
# هر دو عدد زوج هستند.
#
# اگر ورودی‌ها 3 و 8 باشند:
# هر دو عدد زوج نیستند.
#
# اگر ورودی‌ها 5 و 7 باشند:
# هر دو عدد زوج نیستند.
