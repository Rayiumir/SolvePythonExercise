# دریافت دو عدد از ورودی و تبدیل آن‌ها به نوع float
a = float(input("لطفاً عدد اول را وارد کنید: "))
b = float(input("لطفاً عدد دوم را وارد کنید: "))

# محاسبه عملیات ریاضی
sum_result = a + b   # جمع
diff_result = a - b   # تفریق
mult_result = a * b   # ضرب
div_result = a / b   # تقسیم
power_result = a ** b  # توان

# چاپ نتایج
print("جمع =", sum_result)
print("تفریق =", diff_result)
print("ضرب =", mult_result)
print("تقسیم =", div_result)
print("توان =", power_result)

