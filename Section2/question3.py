# دریافت عدد از کاربر و تبدیل آن به عدد صحیح
number = int(input("لطفاً یک عدد وارد کنید: "))

# بررسی زوج یا فرد بودن عدد:
# اگر باقی‌مانده تقسیم عدد بر 2 برابر صفر باشد، عدد زوج است.
if number % 2 == 0:
    print("عدد زوج است.")
else:
    print("عدد فرد است.")

# --------------------------
# مثال خروجی:
# اگر ورودی 6 باشد:
# عدد زوج است.
#
# اگر ورودی 7 باشد:
# عدد فرد است.
# --------------------------
