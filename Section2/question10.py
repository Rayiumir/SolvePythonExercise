# دریافت دو رشته از کاربر
string1 = input("لطفاً رشته اول را وارد کنید: ")
string2 = input("لطفاً رشته دوم را وارد کنید: ")

# بررسی برابر بودن دو رشته
if string1 == string2:
    print("دو رشته با هم برابر هستند.")
else:
    print("دو رشته با هم برابر نیستند.")

# --------------------------
# مثال خروجی:
# اگر ورودی‌ها "hello" و "hello" باشند:
# دو رشته با هم برابر هستند.
#
# اگر ورودی‌ها "apple" و "orange" باشند:
# دو رشته با هم برابر نیستند.
# --------------------------
