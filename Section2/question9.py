# دریافت دو عدد از کاربر و تبدیل آن‌ها به نوع float
num1 = float(input("لطفاً عدد اول را وارد کنید: "))
num2 = float(input("لطفاً عدد دوم را وارد کنید: "))

# بررسی اینکه آیا عدد اول بزرگتر از عدد دوم است
if num1 > num2:
    print("عدد اول بزرگتر از عدد دوم است.")
else:
    print("عدد اول بزرگتر از عدد دوم نیست.")

# --------------------------
# مثال خروجی:
# اگر ورودی‌ها 7 و 5 باشند:
# عدد اول بزرگتر از عدد دوم است.
#
# اگر ورودی‌ها 3 و 8 باشند:
# عدد اول بزرگتر از عدد دوم نیست.
