# دریافت عدد از کاربر و تبدیل آن به نوع float
number = float(input("لطفاً یک عدد وارد کنید: "))

# محاسبه مربع، مکعب، و توان چهارم عدد
square = number ** 2
cube = number ** 3
fourth_power = number ** 4

# چاپ نتایج
print("مربع عدد وارد شده:", square)
print("مکعب عدد وارد شده:", cube)
print("توان چهارم عدد وارد شده:", fourth_power)

# --------------------------
# مثال خروجی:
# اگر ورودی 2 باشد:
# مربع عدد وارد شده: 4.0
# مکعب عدد وارد شده: 8.0
# توان چهارم عدد وارد شده: 16.0
# --------------------------
