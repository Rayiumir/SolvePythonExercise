# دریافت نام کاربر از ورودی
user_name = input("لطفاً نام خود را وارد کنید: ")

# بررسی تعداد حروف نام:
# اگر تعداد حروف بیشتر از ۵ باشد، پیام "نام طولانی است" نمایش داده می‌شود.
if len(user_name) > 5:
    print("نام طولانی است")
else:
    print("نام کوتاه است")

# --------------------------
# مثال خروجی:
# اگر ورودی "محمدرضا" باشد:
# نام طولانی است
#
# اگر ورودی "علی" باشد:
# نام کوتاه است
