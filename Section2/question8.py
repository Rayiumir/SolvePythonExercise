# دریافت عدد صحیح از کاربر
number = int(input("لطفاً یک عدد صحیح وارد کنید: "))

# محاسبه باقی‌مانده تقسیم عدد بر ۳
remainder = number % 3

# نمایش نتیجه
print("باقی‌مانده تقسیم بر ۳ =", remainder)

# --------------------------
# مثال خروجی:
# اگر ورودی 10 باشد:
# باقی‌مانده تقسیم بر ۳ = 1
#
# اگر ورودی 9 باشد:
# باقی‌مانده تقسیم بر ۳ = 0
# --------------------------
