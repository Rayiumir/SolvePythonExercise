# دریافت سه عدد از کاربر
num1 = int(input("لطفاً عدد اول را وارد کنید: "))
num2 = int(input("لطفاً عدد دوم را وارد کنید: "))
num3 = int(input("لطفاً عدد سوم را وارد کنید: "))

# بررسی اینکه آیا مجموع دو عدد اول بزرگتر از عدد سوم است یا خیر
if (num1 + num2) > num3:
    print("مجموع دو عدد اول بزرگتر از عدد سوم است.")
else:
    print("مجموع دو عدد اول بزرگتر از عدد سوم نیست.")

# --------------------------
# مثال خروجی:
# اگر ورودی‌ها 5، 10 و 12 باشند:
# مجموع دو عدد اول بزرگتر از عدد سوم است.
#
# اگر ورودی‌ها 3، 7 و 15 باشند:
# مجموع دو عدد اول بزرگتر از عدد سوم نیست.
