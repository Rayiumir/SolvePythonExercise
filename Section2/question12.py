# دریافت نام از کاربر
name = input("لطفاً نام خود را وارد کنید: ")

# بررسی طول نام و چاپ ۳ حرف اول در صورت نیاز
if len(name) > 3:
    print("سه حرف اول نام شما:", name[:3])
else:
    print("نام شما کمتر از ۴ حرف دارد، نیازی به برش نیست.")

