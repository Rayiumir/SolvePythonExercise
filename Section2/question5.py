# دریافت عدد از کاربر و تبدیل آن به نوع float
number = float(input("لطفاً یک عدد وارد کنید: "))

# محاسبه مربع عدد
square = number ** 2

# چاپ مربع عدد
print("مربع عدد وارد شده:", square)

# --------------------------
# مثال خروجی:
# اگر ورودی 4 باشد:
# مربع عدد وارد شده: 16.0
# --------------------------
