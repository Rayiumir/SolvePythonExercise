# دریافت رشته از کاربر
text = input("لطفاً یک رشته وارد کنید: ")

# بررسی طول رشته
if len(text) <= 4:
    print(text * 5)  # چاپ رشته ۵ بار پشت سر هم
else:
    print("رشته بیش از ۴ حرف دارد، نیازی به تکرار نیست.")

