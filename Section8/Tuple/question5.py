# ایجاد دو tuple از رشته‌ها
tuple1 = ("apple", "banana", "cherry")
tuple2 = ("dog", "elephant", "fox")

# معکوس کردن ترتیب هر tuple
reversed_tuple1 = tuple1[::-1]
reversed_tuple2 = tuple2[::-1]

# نمایش نتیجه
print("tuple1 معکوس:", reversed_tuple1)
print("tuple2 معکوس:", reversed_tuple2)

# نمونه خروجی:
# tuple1 معکوس: ('cherry', 'banana', 'apple')
# tuple2 معکوس: ('fox', 'elephant', 'dog')
