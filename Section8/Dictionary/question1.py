# ایجاد دی‌کشنری از نام‌ها و نمرات دانش‌آموزان
students_grades = {
    "Ali": 18,
    "Sara": 20,
    "Reza": 15,
    "Maryam": 17,
    "Mohammad": 19
}

# محاسبه میانگین نمرات
average_grade = sum(students_grades.values()) / len(students_grades)

# نمایش میانگین نمرات
print("میانگین نمرات دانش‌آموزان:", average_grade)

# نمونه خروجی:
# میانگین نمرات دانش‌آموزان: 17.8
