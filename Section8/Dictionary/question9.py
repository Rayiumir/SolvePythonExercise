# تعریف دی‌کشنری
sample_dict = {
    "apple": 5,
    "banana": 2,
    "orange": 8,
    "grape": 4,
    "kiwi": 6
}

# چاپ تمام کلیدها به ترتیب الفبایی
sorted_keys = sorted(sample_dict.keys())

# نمایش کلیدها
print("کلیدها به ترتیب الفبایی:")
for key in sorted_keys:
    print(key)

