# تعریف دو set از اعداد
set1 = {1, 2, 3, 4, 5, 6}
set2 = {4, 5, 6, 7, 8, 9}

# یافتن اعداد مشترک در هر دو set
common_elements = set1.intersection(set2)

# نمایش نتیجه
print("Set اول:", set1)
print("Set دوم:", set2)
print("اعداد مشترک در هر دو set:", common_elements)

