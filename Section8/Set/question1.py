# ایجاد دو set از اعداد
set1 = {1, 2, 3, 4, 5}
set2 = {4, 5, 6, 7, 8}

# محاسبه تفاوت دو set
difference_set = set1.difference(set2)

# نمایش نتیجه
print("Set 1:", set1)
print("Set 2:", set2)
print("تفاوت دو Set:", difference_set)

