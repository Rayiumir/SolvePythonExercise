# ایجاد set از رشته‌ها
string_set = {"banana", "apple", "cherry", "date", "elderberry"}

# تبدیل set به لیست و مرتب‌سازی به ترتیب حروف الفبا
sorted_string_list = sorted(list(string_set))

# نمایش نتیجه
print("لیست مرتب‌شده به ترتیب حروف الفبا:", sorted_string_list)

