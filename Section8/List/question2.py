# تعریف لیست از رشته‌ها
words = ["apple", "banana", "kiwi", "orange", "grape", "fig"]

# فیلتر کردن رشته‌هایی که بیشتر از ۵ حرف دارند
long_words = [word for word in words if len(word) > 5]

# نمایش لیست جدید
print("رشته‌هایی که بیشتر از ۵ حرف دارند:", long_words)

