# محاسبه مجموع اعداد فرد از 1 تا 100
total_sum = 0
for i in range(1, 101):
    if i % 2 != 0:  # چک می‌کنیم که آیا عدد فرد است
        total_sum += i

# نمایش مجموع
print("مجموع اعداد فرد از 1 تا 100:", total_sum)

