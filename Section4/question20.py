# دریافت عدد از کاربر
num = int(input("لطفاً یک عدد وارد کنید: "))

# چاپ اعداد از 1 تا عدد وارد شده که بر 2 بخش‌پذیر نیستند
print(f"اعداد از 1 تا {num} که بر 2 بخش‌پذیر نیستند عبارتند از:")
for i in range(1, num + 1):
    if i % 2 != 0:
        print(i)

