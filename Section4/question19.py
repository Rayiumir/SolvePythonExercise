# دریافت عدد از کاربر
num = int(input("لطفاً یک عدد وارد کنید: "))

# چاپ مضارب عدد وارد شده تا 100
print(f"مضارب {num} تا 100 عبارتند از:")
for i in range(num, 101, num):  # شروع از num تا 100 با گام num
    print(i)

