# شمارش تعداد اعداد منفی وارد شده
negative_count = 0

while True:
    # دریافت ورودی از کاربر
    number = int(input("یک عدد وارد کنید: "))

    # بررسی اینکه آیا عدد مثبت است یا نه
    if number > 0:
        break  # اگر عدد مثبت بود، حلقه تمام می‌شود
    else:
        negative_count += 1  # اگر عدد منفی بود، شمارش افزوده می‌شود

# نمایش تعداد اعداد منفی وارد شده
print("تعداد اعداد منفی وارد شده:", negative_count)

