# چاپ تمام اعداد مضاعف 7 بین 1 تا 100
for i in range(7, 101, 7):  # شروع از 7 تا 100 با گام 7
    print(i)

# روش دیگر

i = 7
while i <= 100:
    print(i)
    i += 7  # اضافه کردن 7 به عدد فعلی


