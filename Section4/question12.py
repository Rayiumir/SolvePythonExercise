# دریافت سه عدد از کاربر
num1 = int(input("لطفاً عدد اول را وارد کنید: "))
num2 = int(input("لطفاً عدد دوم را وارد کنید: "))
num3 = int(input("لطفاً عدد سوم را وارد کنید: "))

# استفاده از حلقه برای پیدا کردن بزرگترین عدد
numbers = [num1, num2, num3]
max_num = numbers[0]

for num in numbers:
    if num > max_num:
        max_num = num

# نمایش بزرگترین عدد
print("بزرگترین عدد وارد شده:", max_num)

# --------------------------
# خروجی:
# لطفاً عدد اول را وارد کنید: 5
# لطفاً عدد دوم را وارد کنید: 8
# لطفاً عدد سوم را وارد کنید: 3
# بزرگترین عدد وارد شده: 8
