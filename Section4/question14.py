# مقدار اولیه مجموع
sum_of_numbers = 0

# شروع از عدد 1 تا 50
i = 1

while i <= 50:
    sum_of_numbers += i  # افزودن عدد به مجموع
    i += 1  # افزایش مقدار i به عدد بعدی

# نمایش مجموع
print("مجموع اعداد از 1 تا 50:", sum_of_numbers)

