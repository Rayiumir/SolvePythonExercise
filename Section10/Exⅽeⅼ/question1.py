"""
ابتدا کتابخانه openpyxl را با دستور زیر نصب کنید

pip install openpyxl

"""
from openpyxl import Workbook

# ایجاد یک شیء Workbook
wb = Workbook()

# انتخاب برگه فعال
ws = wb.active
ws.title = "اطلاعات افراد"

# وارد کردن هدرها
ws.append(["نام", "سن", "شغل"])

# وارد کردن داده‌ها
data = [
    ["علی", 25, "برنامه نویس"],
    ["مریم", 30, "دکتر"],
    ["امیر", 22, "مهندس"],
    ["زهرا", 28, "مدرس"],
    ["حسین", 35, "مدیر"]
]

# اضافه کردن داده‌ها به برگه
for row in data:
    ws.append(row)

# ذخیره فایل اکسل
wb.save("person_info.xlsx")

# پیامی که نشان دهد فایل ذخیره شده است
print("فایل اکسل با موفقیت ذخیره شد.")
