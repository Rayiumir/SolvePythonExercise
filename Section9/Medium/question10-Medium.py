# 10. فایلی بسازید که پس از نوشتن محتوای آن فایل را باز کرده و محتوای آن به ترتیب چاپ کنید.

# مرحله ۱: یک فایل متنی ایجاد کنید و مقداری محتوا بنویسید
text_content = """پایتون یک زبان برنامه نویسی محبوب است.
مدیریت فایل‌ها یکی از ویژگی‌های مهم آن است.
این تمرین برای یادگیری خواندن و نوشتن فایل‌ها است.
"""

with open("ordered_file.txt", "w", encoding="utf-8") as file:
    file.write(text_content)

# مرحله ۲: فایل را دوباره باز کنید و محتوای آن را بخوانید
with open("ordered_file.txt", "r", encoding="utf-8") as file:
    lines = file.readlines()  # تمام خطوط را در یک لیست می خواند

# مرحله ۳: محتوا را خط به خط به ترتیب چاپ کنید
print("محتویات فایل به ترتیب:")
for line in lines:
    print(line.strip())  # فاصله های اضافی یا کاراکترهای خط جدید را حذف می کند
