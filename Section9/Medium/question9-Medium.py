# 9. یه فایل متنی ایجاد کنید و محتوای آن با استفاده از برنامه به آرایه تبدیل کرده و آن را چاپ کنید.

# مرحله ۱: یک فایل متنی با محتوای نمونه ایجاد کنید
text_content = """پایتون یک زبان برنامه نویسی محبوب است.
کار با فایل ها بسیار مهم است.
یادگیری پایتون آسان و جذاب است.
"""

with open("text_to_array.txt", "w", encoding="utf-8") as file:
    file.write(text_content)

# مرحله ۲: فایل را بخوانید و محتوای آن را به یک آرایه تبدیل کنید
with open("text_to_array.txt", "r", encoding="utf-8") as file:
    lines = [line.strip() for line in file]  # خطوط را بخوانید و فضاهای اضافی را حذف می کند

print("آرایه‌ای از محتوای فایل:")
print(lines)
