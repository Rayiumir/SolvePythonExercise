# 8. یک فایل متنی حاوی تاریخ های مختلف ایجاد کنید. سپس از برنامه ای استفاده کنید که برای تاریخ های مربوط به ماه مهر چاپ کنید.

# لیستی از تاریخ‌های مختلف به فرمت "YYYY-MM-DD"
dates = [
    "1400-09-23",
    "1401-10-01",
    "1402-10-15",
    "1403-11-01",
    "1399-10-20",
    "1398-09-15",
]

# ایجاد فایل و نوشتن تاریخ‌ها در آن
with open("dates.txt", "w") as file:
    for date in dates:
        file.write(date + "\n")

# خواندن داده‌ها از فایل
with open("dates.txt", "r") as file:
    lines = file.readlines()

# چاپ تاریخ‌هایی که مربوط به ماه مهر هستند
print("تاریخ‌های مربوط به ماه مهر:")
for line in lines:
    date = line.strip()
    month = date.split("-")[1]  # استخراج ماه از تاریخ
    if month == "10":  # بررسی اینکه ماه مهر (ماه 10 شمسی) است
        print(date)
