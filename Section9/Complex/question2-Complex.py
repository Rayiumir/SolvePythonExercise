# 2. یک فایل متنی با یک جمله خاص ایجاد کنید، سپس یک جمله جدید به آن اضافه کنید.

# ایجاد فایل متنی و نوشتن جمله خاص در آن
with open("file.txt", "w") as file:
    file.write("این جمله خاص است.\n")

# افزودن یک جمله جدید به فایل
with open("file.txt", "a") as file:
    file.write("این جمله جدید به فایل اضافه شد.\n")
