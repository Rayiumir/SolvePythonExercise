# تعریف یک tuple شامل اعداد
numbers_tuple = (10, 20, 30, 40)

# تبدیل tuple به لیست
numbers_list = list(numbers_tuple)

# افزودن مقدار جدید به لیست
numbers_list.append(50)

# نمایش لیست جدید
print("لیست پس از افزودن مقدار جدید:", numbers_list)

