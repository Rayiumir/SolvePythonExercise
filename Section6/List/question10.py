# دریافت تعداد سطرها و ستون‌ها از کاربر
rows = int(input("تعداد سطرها را وارد کنید: "))
cols = int(input("تعداد ستون‌ها را وارد کنید: "))

# ایجاد لیست دو بعدی
matrix = [[i * j for j in range(cols)] for i in range(rows)]

# نمایش لیست دو بعدی
print("لیست دو بعدی حاصل ضرب اندیس‌ها:")
for row in matrix:
    print(row)

