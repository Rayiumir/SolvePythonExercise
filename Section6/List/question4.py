# دریافت لیست از کاربر
numbers = list(map(int, input("لطفاً اعداد را با فاصله وارد کنید: ").split()))

# معکوس کردن لیست
reversed_numbers = numbers[::-1]

# نمایش لیست معکوس شده
print("لیست معکوس شده:", reversed_numbers)

# --------------------------
# نمونه ورودی:
# لطفاً اعداد را با فاصله وارد کنید: 1 2 3 4 5
# --------------------------
# نمونه خروجی:
# لیست معکوس شده: [5, 4, 3, 2, 1]
# --------------------------
