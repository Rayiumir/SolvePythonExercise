# تعریف لیست از اعداد
numbers = [3, 7, 12, 5, 9]  # مثال لیست اعداد

# محاسبه مجموع اعداد موجود در لیست با استفاده از حلقه for
total_sum = 0
for num in numbers:
    total_sum += num

# نمایش مجموع
print("مجموع اعداد موجود در لیست:", total_sum)

