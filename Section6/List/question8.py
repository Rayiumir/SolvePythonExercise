# # روش اول

# دریافت دو لیست از کاربر
list1 = list(map(int, input("لیست اول را وارد کنید (اعداد را با فاصله وارد کنید): ").split()))
list2 = list(map(int, input("لیست دوم را وارد کنید (اعداد را با فاصله وارد کنید): ").split()))

# ایجاد یک لیست جدید و استفاده از extend برای اضافه کردن لیست دوم
list1.extend(list2)

# نمایش لیست جدید
print("لیست ادغام شده:", list1)

# # روش دوم

# دریافت دو لیست از کاربر
list1 = list(map(int, input("لیست اول را وارد کنید (اعداد را با فاصله وارد کنید): ").split()))
list2 = list(map(int, input("لیست دوم را وارد کنید (اعداد را با فاصله وارد کنید): ").split()))

# ادغام دو لیست
merged_list = list1 + list2

# نمایش لیست جدید
print("لیست ادغام شده:", merged_list)

