# دریافت لیست از رشته‌ها از کاربر
strings = input("لیست رشته‌ها را وارد کنید (رشته‌ها را با فاصله وارد کنید): ").split()

# مرتب‌سازی لیست به ترتیب حروف الفبا
strings.sort()

# نمایش لیست مرتب شده
print("لیست مرتب شده:", strings)

