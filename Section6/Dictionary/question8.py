# تعریف دیکشنری شامل نام دانش‌آموزان و نمرات آنها
students_scores = {
    "علی": 18,
    "زهرا": 14,
    "محمد": 16,
    "سارا": 12,
    "حسین": 19
}

# فیلتر کردن دانش‌آموزانی که نمره بالای ۱۵ دارند
top_students = {name: score for name, score in students_scores.items() if score > 15}

# نمایش دانش‌آموزان با نمره بالای ۱۵
print("دانش‌آموزانی که نمره بالای ۱۵ دارند:", top_students)

