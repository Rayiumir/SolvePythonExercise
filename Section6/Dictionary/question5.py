# تعریف یک متن نمونه
text = "برنامه نویسی پایتون بسیار جذاب است و برنامه نویسی کاربردی است"

# تقسیم متن به لیست کلمات
words = text.split()

# ایجاد دیکشنری برای شمارش تکرار کلمات
word_count = {}

# شمارش تکرار هر کلمه با استفاده از یک حلقه for
for word in words:
    word_count[word] = word_count.get(word, 0) + 1

# نمایش دیکشنری شامل کلمات و تعداد تکرار آنها
print(word_count)

