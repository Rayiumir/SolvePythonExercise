# تعریف دیکشنری شامل نام و شماره تماس افراد
contacts = {
    "علی": "09123456789",
    "زهرا": "09123456780",
    "محمد": "09123456781",
    "سارا": "09123456782"
}

# نام فردی که شماره تماسش را می‌خواهیم پیدا کنیم
name_to_find = "محمد"

# پیدا کردن شماره تماس فرد خاص
phone_number = contacts.get(name_to_find, "فرد مورد نظر پیدا نشد.")

# نمایش شماره تماس
print(f"شماره تماس {name_to_find}: {phone_number}")

