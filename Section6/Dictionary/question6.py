# تعریف دیکشنری شامل محصولات و قیمت آن‌ها
products = {
    "لپ‌تاپ": 25000000,
    "گوشی": 15000000,
    "تبلت": 12000000,
    "ساعت هوشمند": 8000000
}

# یافتن گران‌ترین محصول
most_expensive = max(products, key=products.get)

# نمایش نتیجه
print("گران‌ترین محصول:", most_expensive, "با قیمت", products[most_expensive])

