# تعریف دو set
set1 = {1, 2, 3, 4, 5}
set2 = {2, 3, 4}

# بررسی اینکه آیا set2 زیرمجموعه set1 هست یا نه
if set2.issubset(set1):
    print("set2 یک زیرمجموعه از set1 است.")
else:
    print("set2 زیرمجموعه set1 نیست.")

