# تعریف یک set از اعداد
numbers = {10, 20, 30, 40, 50}

# مقدار موردنظر برای بررسی
target = 30

# بررسی وجود مقدار در set
if target in numbers:
    print(f"{target} در set وجود دارد.")
else:
    print(f"{target} در set وجود ندارد.")

