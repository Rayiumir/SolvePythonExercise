# 19. یک عدد اعشاری را به عنوان ورودی گرفته و به نزدیکترین عدد صحیح گرد کرده و نمایش دهد.

decimal_number = float(input("Enter a decimal number: "))
rounded_number = round(decimal_number)

print("The rounded number is:", rounded_number)

